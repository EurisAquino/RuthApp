    public void buscarTabla(){
        String campo = txtNombre.getText();

        DefaultTableModel modeloTabla = (DefaultTableModel) JTableMercancia.getModel();
        modeloTabla.setRowCount(0);

        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;

        int columnas;

        int[] anchos = {5, 30, 10, 20};

        for (int i = 0; i < JTableMercancia.getColumnCount(); i++) {
            JTableMercancia.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }

        try {
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT id,nombre,cantidad,precio FROM productos WHERE nombre = '"
                    + campo + "'");

            System.out.println(ps);

            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();

            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int indice = 0; indice < columnas; indice++) {
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }
        } catch (SQLException e) {
        }
    }

    public void cargarTabla() {

        DefaultTableModel modeloTabla = (DefaultTableModel) JTableMercancia.getModel();
        modeloTabla.setRowCount(0);

        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;

        int columnas;

        int[] anchos = {10,31, 10, 20};

        for (int i = 0; i < JTableMercancia.getColumnCount(); i++) {
            JTableMercancia.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }

        try {
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT id,nombre,cantidad,precio FROM productos");

            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();

            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int indice = 0; indice < columnas; indice++) {
                    fila[indice] = rs.getObject(indice + 1);
                }
                modeloTabla.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        

    }
