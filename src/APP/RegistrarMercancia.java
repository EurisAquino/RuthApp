    private void JTableMercanciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableMercanciaMouseClicked
       
        PreparedStatement ps;
        ResultSet rs;

        try {
            Conexion con = new Conexion();
            Connection conn = Conexion.getConexion();

            int fila = JTableMercancia.getSelectedRow();
            int id = Integer.parseInt(JTableMercancia.getValueAt(fila, 0).toString());

            ps = conn.prepareStatement("SELECT id,nombre,cantidad,precio FROM productos WHERE id =?");

            ps.setInt(1, id);
            rs = ps.executeQuery();

            while (rs.next()) {
                txtId.setText(rs.getString("id"));
                txtNombre.setText(rs.getString("nombre"));
                txtCantidad.setText(rs.getString("cantidad"));
                txtPrecio.setText(rs.getString("precio"));    
            }
            } catch (SQLException e) {
            System.out.println(e.toString());
        }   
        
    }//GEN-LAST:event_JTableMercanciaMouseClicked

